<script>
  // Carrossel simples: rolagem horizontal com botões
  (function(){
    const viewport = document.getElementById('evoCarousel');
    if (!viewport) return;
    const track = viewport.querySelector('.evo-track');
    const cards = viewport.querySelectorAll('.evo-card');
    const prev = document.querySelector('.evo-controls [data-dir="prev"]');
    const next = document.querySelector('.evo-controls [data-dir="next"]');

    function cardWidth(){
      const first = cards[0];
      const style = window.getComputedStyle(first);
      const gap = parseFloat(style.marginRight) || 16;
      return first.getBoundingClientRect().width + gap;
    }

    function scrollByDir(dir){
      viewport.scrollBy({ left: (dir==='next'? 1 : -1) * cardWidth(), behavior: 'smooth' });
    }

    prev && prev.addEventListener('click', () => scrollByDir('prev'));
    next && next.addEventListener('click', () => scrollByDir('next'));

    // setas via teclado
    viewport.addEventListener('keydown', (e)=>{
      if (e.key === 'ArrowRight') scrollByDir('next');
      if (e.key === 'ArrowLeft')  scrollByDir('prev');
    });
  })();
</script>


