      <h2 class="gf-h2">Histórico</h2>
      <p class="tex-jus"><strong>Marjorie “Miss” Craig</strong>, norte-americana, é reconhecida como a criadora da Ginástica Facial. Com formação em Educação Física, interessou-se, nas décadas de 1930 e 1940, pelas relações entre glândulas, musculatura do rosto e expressão. Estudou casos de pacientes com <strong>paralisia facial</strong>, com sequelas decorrentes de cirurgias e traumas, e sistematizou exercícios específicos para a face, pescoço e região cervical, atuando em clínicas e hospitais de Nova York. Trabalhou com crianças e adolescentes, além de orientar atores e cantores quanto à expressividade muscular do rosto.</p>
      <p class="tex-jus">Seu programa influenciou profissionais de várias áreas e foi difundido internacionalmente. No Brasil, a técnica ganhou novas pesquisas e aplicação clínica, aliando observação prática e bases científicas. Esse é o legado que sustenta, até hoje, o <strong>Programa de Ginástica Facial</strong> aplicado por Bartira Bravo.</p>
